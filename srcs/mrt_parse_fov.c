#include "minirt.h"

int	parse_fov(int *fov, char *s)
{
	char	*tmp;

	tmp = s;
	if (check_atoi(s))
		return (-1);
	*fov = ft_atoi(s);
	if (*fov < 0 || *fov > 180)
		return (-1);
	return (0);
}
