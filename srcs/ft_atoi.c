#include "minirt.h"

int	ft_atoi(char *s)
{
	int			minus;
	int			ans;

	ans = 0;
	minus = 1;
	if (!s)
		return (0);
	while (*s == ' ' || *s == '\t' || *s == '\n' ||
		*s == '\r' || *s == '\v' || *s == '\f')
		s++;
	if (*s == '-' || *s == '+')
	{
		if (*s == '-')
			minus = -1;
		s++;
	}
	while (*s >= 48 && *s <= 57)
	{
		ans = ans * 10 + *s - 48;
		s++;
	}
	return (ans * minus);
}
